/**
 * Get return history for an order or customer.
 */
export async function getReturnHistory(
  filters: { orderId?: string; customerId?: string }
): Promise<ReturnRequest[]> {
  // In a production system, this would query a ReturnRequest model
  // For now, we look at orders with returned/cancelled status
  const where: Record<string, unknown> = {};
  if (filters.orderId) where.id = filters.orderId;
  if (filters.customerId) where.customerId = filters.customerId;
  where.status = { in: ['returned', 'cancelled'] };

  const orders = await db.order.findMany({
    where,
    include: { items: true },
    orderBy: { createdAt: 'desc' },
  });

  // Map orders to return request format
  return orders.map((order) => ({
    id: order.id,
    orderId: order.id,
    customerId: order.customerId,
    reason: 'Customer return',
    reasonAr: 'إرجاع العميل',
    items: order.items.map((oi) => ({
      productId: oi.productId,
      productNameEn: oi.productNameEn,
      productNameAr: oi.productNameAr,
      quantity: oi.quantity,
      unitPrice: oi.unitPrice as Piastres,
      refundAmount: (oi.quantity * oi.unitPrice) as Piastres,
      condition: 'sellable' as const,
      restocked: false,
    })),
    totalRefundAmount: order.totalAmount as Piastres,
    status: 'initiated' as ReturnStatus,
    initiatedAt: order.createdAt.toISOString(),
    processedAt: null,
  }));
}
