// RETURN INITIATION
// ============================================

/**
 * Initiate a return for an order.
 * Validates that the order is eligible for return (within return window).
 */
export async function initiateReturn(
  orderId: string,
  customerId: string,
  reason: string,
  reasonAr: string = '',
  items: Array<{ productId: string; quantity: number }>
): Promise<ReturnRequest> {
  // Verify order exists and belongs to customer
  const order = await db.order.findUnique({
    where: { id: orderId },
    include: {
      items: true,
      paymentTransactions: true,
    },
  });

  if (!order) {
    throw new NotFoundError('Order not found', 'ORDER_NOT_FOUND');
  }

  if (order.customerId !== customerId) {
    throw new ForbiddenError('Order does not belong to this customer', 'ORDER_NOT_OWNED');
  }

  // Check return window
  const returnDeadline = new Date(order.createdAt);
  returnDeadline.setDate(returnDeadline.getDate() + RETURN_WINDOW_DAYS);

  if (new Date() > returnDeadline) {
    throw new ValidationError(
      `Return window expired. Returns must be initiated within ${RETURN_WINDOW_DAYS} days`,
      'RETURN_WINDOW_EXPIRED'
    );
  }

  // Order must be delivered to be eligible for return
  if (order.status !== 'delivered') {
    throw new ValidationError(
      'Only delivered orders can be returned',
      'ORDER_NOT_DELIVERED'
    );
  }

  // Validate items — must exist in order and quantity must be valid
  const returnItems: ReturnItem[] = [];
  for (const requestedItem of items) {
    const orderItem = order.items.find(
      (oi: { productId: string }) => oi.productId === requestedItem.productId
    );
    if (!orderItem) {
      throw new NotFoundError(
        `Product ${requestedItem.productId} not found in order`,
        'PRODUCT_NOT_IN_ORDER'
      );
    }
    if (requestedItem.quantity > orderItem.quantity) {
      throw new ValidationError(
        `Return quantity exceeds order quantity for product ${requestedItem.productId}`,
        'RETURN_QUANTITY_EXCEEDS'
      );
    }

    returnItems.push({
      productId: requestedItem.productId,
      productNameEn: orderItem.productNameEn,
      productNameAr: orderItem.productNameAr,
      quantity: requestedItem.quantity,
      unitPrice: orderItem.unitPrice as Piastres,
      refundAmount: (requestedItem.quantity * orderItem.unitPrice) as Piastres,
      condition: 'sellable', // Default condition, updated during inspection
      restocked: false,
    });
  }

  // Calculate total refund
  const totalRefundAmount = returnItems.reduce(
    (sum, item) => (sum + item.refundAmount) as Piastres,
    0 as Piastres
  );

  const returnRequest: ReturnRequest = {
    id: '', // Will be generated by DB
    orderId,
    customerId,
    reason,
    reasonAr,
    items: returnItems,
    totalRefundAmount,
    status: 'initiated',
    initiatedAt: new Date().toISOString(),
    processedAt: null,
  };

  logger.info('Return initiated', { orderId, customerId, itemsCount: items.length });

  return returnRequest;
}

// ============================================
// REFUND CALCULATION
// ============================================

/**
 * Calculate refund amount for a return request.
 * Includes item refunds, delivery fee refund logic, and any deductions.
 */
export async function calculateRefund(
  orderId: string,
  returnItemIds: Array<{ productId: string; quantity: number }>,
  fullReturn: boolean = false
): Promise<RefundCalculation> {
  const order = await db.order.findUnique({
    where: { id: orderId },
    include: {
      items: true,
      paymentTransactions: {
        where: { status: 'paid' },
        orderBy: { createdAt: 'desc' },
        take: 1,
      },
    },
  });

  if (!order) {
    throw new NotFoundError('Order not found', 'ORDER_NOT_FOUND');
  }

  const totalPaid = order.totalAmount as Piastres;

  // Calculate items refund
  let itemsRefund = 0 as Piastres;
  for (const returnItem of returnItemIds) {
    const orderItem = order.items.find(
      (oi: { productId: string }) => oi.productId === returnItem.productId
    );
    if (orderItem) {
      itemsRefund = (itemsRefund + returnItem.quantity * orderItem.unitPrice) as Piastres;
    }
  }

  // Delivery fee refund — only for full returns
  const deliveryFeeRefund: Piastres = fullReturn
    ? (order.deliveryFee as Piastres)
    : (0 as Piastres);

  // Deduction calculation (e.g., restocking fees for partial returns)
  const deduction = 0 as Piastres;
  const deductionReason = '';

  const totalRefund = (itemsRefund + deliveryFeeRefund - deduction) as Piastres;

  // Validate refund doesn't exceed total paid
  if (totalRefund > totalPaid) {
    // Cap at total paid
    return {
      orderId,
      totalPaid,
      itemsRefund,
      deliveryFeeRefund,
      totalRefund: totalPaid,
      deduction: (totalRefund - totalPaid) as Piastres,
      deductionReason: 'Refund capped at total paid amount',
    };
  }

  return {
    orderId,
    totalPaid,
    itemsRefund,
    deliveryFeeRefund,
    totalRefund,
    deduction,
    deductionReason,
  };
}

