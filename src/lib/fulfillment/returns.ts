/**
 * Process restocking for returned items.
 * Increments product stock for sellable items.
 */
export async function restockItems(
  items: Array<{ productId: string; quantity: number; condition: 'sellable' | 'damaged' | 'expired' }>
): Promise<void> {
  for (const item of items) {
    if (item.condition === 'sellable') {
      await db.product.update({
        where: { id: item.productId },
        data: { stock: { increment: item.quantity } },
      });

      logger.info('Product restocked', {
        productId: item.productId,
        quantity: item.quantity,
      });
    }
  }
}

